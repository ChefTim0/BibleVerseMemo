export function getBookName(language: string, bookAbbr: string): string {
  const mappedLang = getLanguageFromBibleVersion(language);
  const langBooks = bookNames[mappedLang as keyof typeof bookNames];
  
  const normalizeBookKey = (key: string): string => {
    const bookKeys = Object.keys(bookNames.en);
    const found = bookKeys.find(k => k.toLowerCase() === key.toLowerCase());
    return found || key;
  };
  
  const normalizedKey = normalizeBookKey(bookAbbr);
  
  if (langBooks && langBooks[normalizedKey as keyof typeof langBooks]) {
    return langBooks[normalizedKey as keyof typeof langBooks];
  }
  const defaultBooks = bookNames['en'] || bookNames['fr'];
  return defaultBooks?.[normalizedKey as keyof typeof defaultBooks] || bookAbbr;
}

export function t(language: string, key: string): string {
  const langTranslations = translations[language as keyof typeof translations];
  if (langTranslations && langTranslations[key]) {
    return langTranslations[key];
  }
  return translations['en']?.[key] || translations['fr']?.[key] || key;
}
